const MyNFTGallery = () => {
    const [nfts, setNfts] = useState([]);
    const [tokenId, setTokenId] = useState(null);
    const [isLoading, setIsLoading] = useState(true);
    const [error, setError] = useState(null);


    const handleSellButtonClick = (token) => {
        setTokenId(Number(token));
    };
    useEffect(() => {
        const fetchNFTs = async () => {
            if (typeof window.ethereum === 'undefined') {
                console.error("MetaMask is not installed");
                setError("MetaMask is not installed");
                setIsLoading(false);
                return;
            }

            try {
                const web3 = new Web3(window.ethereum);
                const accounts = await window.ethereum.request({ method: 'eth_requestAccounts' });
                const connectedAddress = accounts[0];

                const nftContract = new web3.eth.Contract(contractABI, contractAddress);

                const nftTokenIds = await nftContract.methods.getTokenId(connectedAddress).call();
                console.log(nftTokenIds);

                if (Array.isArray(nftTokenIds)) {
                    const nftDetails = await Promise.all(nftTokenIds.map(async (nft) => {
                        try {
                            const nftUri = await nftContract.methods.getTokenUri(nft.tokenId).call();
                            console.log(nftUri);
                            const response = await fetch(nftUri);
                            const metadata = await response.json();
                            return {
                                tokenId: nft.tokenId,
                                ...metadata
                            };
                        } catch (error) {
                            console.error('Error fetching or parsing metadata:', error);
                            return null; 
                        }
                    }));

                    const validNftDetails = nftDetails.filter(nft => nft !== null);
                    setNfts(validNftDetails);
                }
            } catch (error) {
                console.error('Error fetching NFTs:', error);
                setError(error.message);
            } finally {
                setIsLoading(false);
            }
        };
        fetchNFTs();


    }, []);
   
    const handleApproveNft = async(approveTokens)=>{
        try {
            const web3 = new Web3(window.ethereum);
            const accounts = await window.ethereum.request({ method: 'eth_requestAccounts' });
            const connectedAddress = accounts[0];
            const Contract = new web3.eth.Contract(contractABI, contractAddress);
            const approveToken = (Number(approveTokens));
            console.log(approveTokens,marketContract)
            const tx = await Contract.methods.approve(marketContract,approveToken).send({ from: connectedAddress });
            if (tx) {
                Swal.fire(
                    'Success!',
                    'Your NFT was Approved successfully.',
                    'success'
                );
            } else {
                Swal.fire(
                    'Failed!',
                    'Failed to Approved NFT.',
                    'error'
                );
            }
    
        } catch (error) {
            console.error('Error the Approve Nft', error);
        }
    }

    return (
        <div>
       <NftMint/>
       <ListLandNft id={tokenId}/>
            <div className="container-fluid mb-4">
                <Navbar/> 
                <h1 className='text-center m-5'>Admin Nft MarketPlace</h1>
                <div className="row">
                   <table class="table" style={{height:"500px",overflow:'scroll'}}>
                     <thead>
                        <button className='btn btn-dark' data-bs-toggle="modal" data-bs-target="#mintnft">SafeMint</button>
                       <tr>
                         <th scope="col">Token Id</th>
                         <th scope="col">Nft Name</th>
                         <th scope="col">Nft Picture</th>
                         <th scope="col">Nft Description</th>
                         <th scope="col">Nft Approve</th>
                         <th scope="col">Nft SetValue</th>
                       </tr>
                     </thead>
                     <tbody >
                     {nfts.length > 0 ? (
                        nfts.map(nft => (
                            <tr className='border'>
                            <td>{Number(nft.tokenId)}</td>
                            <td >{nft.name}</td>
                            <td><img src={nft.image} height={80} width={100} alt=""/></td>
                            <td>{nft.description}</td>
                            <td><button className='btn btn-primary' onClick={()=>handleApproveNft(nft.tokenId)}>Nft Approve</button></td>
                            <td><button className='btn btn-success' data-bs-toggle="modal" data-bs-target="#ListLandNft" onClick={()=>handleSellButtonClick(nft.tokenId)}>ListLandNft</button></td>
                          </tr>
                        ))
                    ) : (
                        !isLoading && <p>No NFTs found.</p>
                    )}
                      
                     </tbody>
                   </table>
                </div>
            </div>
        </div>
    );
};

export default MyNFTGallery;
