const CheckBlance = () => {
  const [web3, setWeb3] = useState(null);
  const [contract, setContract] = useState(null);
  const [account, setAccount] = useState('');
  const [allbalance, setBalance] = useState('0');

  const initWeb3 = async () => {
    const provider = await detectEthereumProvider();
    if (provider) {
      const web3Instance = new Web3(provider);
      setWeb3(web3Instance);
      
      const accounts = await web3Instance.eth.requestAccounts();
      setAccount(accounts[0]);
      const contractInstance = new web3Instance.eth.Contract(abi, contractAddress);
      setContract(contractInstance);
      console.log('Contract:', contractInstance);
      checkBalance(accounts[0]);
    } else {
      console.error('Please install MetaMask!');
    }
  };

  const mintTokens = async () => {
    if (contract) {
      try {
        await contract.methods.mint().send({ from: account });
        console.log('Tokens minted');
        checkBalance(account);  
      } catch (error) {
        console.error('Error minting tokens:', error);
      }
    }
  };

  const transferTokens = async (to, amount) => {
    if (contract) {
      try {
        await contract.methods.transfer(to, amount).send({ from: account });
        console.log('Tokens transferred');
        checkBalance(account);  // Update balance after transferring
      } catch (error) {
        console.error('Error transferring tokens:', error);
      }
    }
  };

  const checkBalance = async (address) => {
    if (contract) {
      try {
        const balance = await contract.methods.balanceOf(address).call();
        setBalance(balance);
        console.log(`Balance of ${address}: ${balance}`);
      } catch (error) {
        console.error('Error checking balance:', error);
      }
    }
  };

  useEffect(() => {
    if (account) {
      initWeb3();
    }
  }, [account]);

  return (
    <div>
      <button className='btn btn-dark m-4' onClick={initWeb3}>Connect Wallet</button>
      <button className='btn btn-primary m-4' onClick={mintTokens}>Mint Tokens</button>
      <button className='btn btn-success m-4' onClick={() => transferTokens('0x8Eb6682c152Aa8452231D19F7070BF5c4D325BB3', 100)}>Transfer Tokens</button>
      <button className='btn btn-danger m-4' onClick={() => checkBalance(account)}>Check Balance</button>
      <div>Connected Account: {account}</div>
    </div>
  );
};

export default CheckBlance;
